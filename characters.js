const MEGIDO_LIST = [];
RAW_MEGIDO_DATA.trim().split('\n').forEach(line => {
    const match = line.match(/^(\S+)\s+(.+)$/);
    if (match) {
        MEGIDO_LIST.push({ id: match[1], name: match[2] });
    }
});

const categoryOrder = { "祖": 1, "真": 2, "継": 3, "宵": 4 };

// 祖・真・継・宵の順、かつ番号順にソート
MEGIDO_LIST.sort((a, b) => {
    const catA = a.id.charAt(0);
    const catB = b.id.charAt(0);
    const numA = parseInt(a.id.slice(1)) || 0;
    const numB = parseInt(b.id.slice(1)) || 0;
    
    if (categoryOrder[catA] !== categoryOrder[catB]) {
        return (categoryOrder[catA] || 99) - (categoryOrder[catB] || 99);
    }
    if (numA !== numB) {
        return numA - numB;
    }
    // 番号が同じ場合は、無印を先にする（例: リリム -> リリムR）
    return a.name.length - b.name.length;
});

// ゲーム判定用（末尾アルファベット除去・重複除去）の配列を自動生成
const uniqueNames = new Set();
MEGIDO_LIST.forEach(m => {
    uniqueNames.add(m.name.replace(/[CRB]$/, ''));
});
const MEGIDO_CHARACTERS = Array.from(uniqueNames);
